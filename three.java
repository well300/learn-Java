///---Java Variables---///
/*
* Variables are containers for storing data values.

* In Java, there are different types of variables, for example:

* String - stores text, such as "Hello". String values are surrounded by double quotes
* int - stores integers (whole numbers), without decimals, such as 123 or -123
* float - stores floating point numbers, with decimals, such as 19.99 or -19.99
* char - stores single characters, such as 'a' or 'B'. Char values are surrounded by single quotes
* boolean - stores values with two states: true or false
*/

//#Declaring (Creating) Variables
//Syntax => type variableName = value;

public class three{
    public static void main(String[] args){

        int num = 5;          //int
        float Fnum = 5.5f;    // float
        char Cword = 'O';     // CHar
        boolean myB = true;   //boolean
        String name = "Talha"; //String


        System.out.println(num); //declaring int
        System.out.println(Fnum);// declaring float
        System.out.println(Cword);// declaring char
        System.out.println(myB);// declaring boolean
        System.out.println(name);// declaring string
    }
    }
